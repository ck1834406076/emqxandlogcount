    public  <T> ResponseEntity<T> uploadBaiDuPlayList(String url, Class<T> responseType, String filePath) {
        //设置请求头
        HttpHeaders headers = new HttpHeaders();
        MediaType type = MediaType.parseMediaType("multipart/form-data");
        headers.setContentType(type);
        //转为FileSystemResource 用于发送文件
        FileSystemResource fileSystemResource = new FileSystemResource(filePath);
        //设置请求体，注意是LinkedMultiValueMap
        MultiValueMap<String, Object> form = new LinkedMultiValueMap<>();
        form.add("Name", fileSystemResource.getFilename());
        form.add("OriginFileName", fileSystemResource.getFilename());
        form.add("path", fileSystemResource);
        form.add("Type", 1);
        //用HttpEntity封装整个请求报文
        HttpEntity<MultiValueMap<String, Object>> files = new HttpEntity<>(form, headers);
        return restTemplate.postForEntity(url, files, responseType);
    }

